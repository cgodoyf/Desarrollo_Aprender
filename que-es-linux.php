				<h2>¿Que es kernel de linux?</h2>
				<p>El kernel ó núcleo de linux se puede definir como el corazón de este sistema operativo. Es el encargado de que el software y el hardware de tu ordenador puedan trabajar juntos.</p>
				<br>
				<p>Las funciones más importantes del mismo, aunque no las únicas, son:</p>
				<br>
				<li>Administración de la memoria para todos los programas y procesos en ejecución.</li>
				<br>
				<li>Administración del tiempo de procesador que los programas y procesos en ejecucion utilizan.</li>
				<br>
				<li>Es el encargado de que podamos acceder a los periféricos/elementos de nuestro ordenador de una manera cómoda.</li>
				<br>
				<p class="autor">-Linux-es 2015-</p>
