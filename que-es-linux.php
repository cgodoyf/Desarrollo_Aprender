				<h2>¿Por qué usar Linux?</h2>
				<p>Los usuarios buscan sistemas que puedan satisfacer sus necesidades. Tal vez la curiosidad los ha llevado a buscar en GNU/Linux, pero es bueno saber que la elección no solamente puede satisfacer dicha curiosidad.</p>
				<br>
				<p>Como estudiantes, tendrán ante ustedes un sistema muy configurable y esa sola opción hace que sea el preferido de los ámbitos académicos.</p>
				<br>
				<p>El poder tener el código fuente de cada punto de un sistema operativo funcional es un recurso invaluable que no se podía tener hasta no hace mucho. Pero no nos engañemos con que tenemos un sistema solamente didáctico. GNU/Linux presenta un sin fin de aplicaciones que generan un ambiente por demás de estable para los negocios de hoy en día. Y este es un punto interesante ya que es en las empresas donde GNU/Linux tiene mayor entrada, debido en gran parte al poder contar con un sistema Un*x de bajo costo total de la propiedad (TCO por sus siglas en inglés) y sin las complicaciones de licencias que tienen otros SO.</p>
